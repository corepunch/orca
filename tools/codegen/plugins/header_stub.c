#include "cg_api.h"

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

static int emit_header(cg_host_v1 const *host, cg_model const *model, char const *output) {
    char buf[8192];
    char mod[256];
    size_t i;
    size_t n = 0u;
    uint32_t kinds[16] = {0};

    for (i = 0; i < model->row_count; ++i) {
        cg_kind k = model->rows[i].kind;
        if (k < 16u) kinds[k]++;
    }

    snprintf(mod, sizeof(mod), "%s", (model->module_name && model->module_name[0]) ? model->module_name : "module");
    for (i = 0; mod[i]; ++i) if (mod[i] >= 'a' && mod[i] <= 'z') mod[i] = (char)(mod[i] - 32);

    n += (size_t)snprintf(buf + n, sizeof(buf) - n,
        "/* Generated by cgen header_stub plugin */\n"
        "#ifndef ORCA_%s_GENERATED_H\n"
        "#define ORCA_%s_GENERATED_H\n\n"
        "/* Source: %s */\n"
        "/* rows=%zu classes=%u structs=%u interfaces=%u enums=%u */\n\n"
        "#endif\n",
        mod, mod,
        model->xml_path,
        model->row_count,
        kinds[CG_KIND_CLASS],
        kinds[CG_KIND_STRUCT],
        kinds[CG_KIND_INTERFACE],
        kinds[CG_KIND_ENUM]);

    if (n >= sizeof(buf)) {
        host->logf("header_stub: output overflow for %s", output);
        return -1;
    }
    return host->write_file(output, buf, n);
}

static cg_plugin_v1 const plugin = {
    .abi = CG_API_VERSION,
    .name = "header_stub",
    .emit = emit_header
};

cg_plugin_v1 const *cg_plugin_init_v1(void) {
    return &plugin;
}
